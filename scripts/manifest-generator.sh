main() {
    case "${1:-help}" in
        "stack")
            if [[ $# -lt 2 ]]; then
                echo -e "${RED}‚ùå Stack name required${NC}"
                print_usage
                exit 1
            fi
            generate_stack_manifest "$2" "${3:-}"
            ;;
        "component")
            if [[ $# -lt 2 ]]; then
                echo -e "${RED}‚ùå Component name required${NC}"
                print_usage
                exit 1
            fi
            generate_component_manifest "$2" "${3:-}" "${4:-}"
            ;;
        "template")
            if [[ $# -lt 3 ]]; then
                echo -e "${RED}‚ùå Template type and name required${NC}"
                print_usage
                exit 1
            fi
            case "$2" in
                "stack")
                    generate_stack_template "$3" "${4:-}" "${5:-}" "${6:-}" "${7:-}"
                    ;;
                "component")
                    generate_component_template "$3"
                    ;;
                *)
                    echo -e "${RED}‚ùå Unknown template type: $2${NC}"
                    echo -e "${YELLOW}Available types: stack, component${NC}"
                    exit 1
                    ;;
            esac
            ;;
        "resource")
            if [[ $# -lt 2 ]]; then
                echo -e "${RED}‚ùå Resource type required${NC}"
                print_usage
                exit 1
            fi
            generate_resource_template "$2" "${3:-}"
            ;;
        "list")
            list_templates
            ;;
        "help"|*)
            print_usage
            ;;
    esac
}

main "$@"